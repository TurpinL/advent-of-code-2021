val openingChars = "([{<"
val closingChars = ")]}>"
val scores = listOf(3, 57, 1197, 25137)

var currentChunk = ""
var isCurrentChunkCorrupted = false
var corruptEnds = mutableListOf<Char>()
val incompleteChunks = mutableListOf<String>()

for (line in input().lines()) {
    isCurrentChunkCorrupted = false
    currentChunk = ""

    for (char in line) {
        if (char in openingChars) {
            currentChunk += char
        } else {
            if (closingChars.indexOf(char) == openingChars.indexOf(currentChunk.last())) {
                currentChunk = currentChunk.take(currentChunk.length - 1)
            } else {
                corruptEnds.add(char)
                isCurrentChunkCorrupted = true
                break
            }
        }
    }

    if (!isCurrentChunkCorrupted) {
        incompleteChunks.add(currentChunk)
    }
}

println("$corruptEnds")
println("syntax error score (part 1)")
corruptEnds.sumOf {
    scores[closingChars.indexOf(it)]
}

val autocompleteScores = mutableListOf<Long>()
for (chunk in incompleteChunks) {
    var score = 0L

    for (char in chunk.reversed()) {
        score *= 5
        score += openingChars.indexOf(char) + 1
    }

    autocompleteScores.add(score)
    println ("$chunk = $score")
}

println("autocomplete score (part 2)")
autocompleteScores.sorted()[autocompleteScores.size/2]


